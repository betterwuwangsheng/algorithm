#include <iostream>

using namespace std;

int t, n, m, q, u, v, k;
int main() {
    scanf("%d", &t);
    while (t--) {
        scanf("%d%d%d", &n, &m, &q);

        while (m--) {
            scanf("%d %d", &u, &v);
        }
        while (q--) {
            scanf("%d", &k);
        }
    }
    return 0;
}