#include <iostream>

using namespace std;

int main()
{
    int T;
    scanf("%d", &T);

    int n, m;
    while (T--)
    {
        scanf("%d %d", &n, &m);

        for (int i = 0; i < n; ++i)
        {
            printf("sto ");
        }
        printf("7");
        for (int i = 0; i < m; ++i)
        {
            printf(" orz");
        }
        printf("\n");
    }
    return 0;
}