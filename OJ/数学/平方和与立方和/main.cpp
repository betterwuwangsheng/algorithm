#include <iostream>
#include<cstdio>
#include<cmath>
using namespace std;

/**
Problem Description
给定一段连续的整数，求出他们中所有偶数的平方和以及所有奇数的立方和。


Input
输入数据包含多组测试实例，每组测试实例包含一行，由两个整数m和n组成。


Output
对于每组输入数据，输出一行，应包括两个整数x和y，分别表示该段连续的整数中所有偶数的平方和以及所有奇数的立方和。
你可以认为32位整数足以保存结果。


Sample Input
1 3
2 5


Sample Output
4 28
20 152

*/
int main() {
    int m,n,evenSunOfSquares,oddCubeSum;
    while(scanf("%d %d", &m, &n) != EOF) {
        evenSunOfSquares =0;
        oddCubeSum = 0;

        //边界条件判断
        if(m > n) {
            int temp = m;
            m = n;
            n = temp;
        }
        for(int i = m; i <= n; i++ )
            if(i % 2 == 0) { //偶数
                evenSunOfSquares += i * i;
            } else {
                oddCubeSum += i * i * i;
            }
        printf("%d %d\n",evenSunOfSquares,oddCubeSum);
    }
    return 0;
}
